# Write a class to represent a company.

class Company:
    def __init__(self, name, location, ceo, manager, employee, office, canteen, parking, conference_room):
        self.name = name
        self.location = location
        self.ceo = ceo
        self.manager = manager
        self.employee = employee
        self.office = office
        self.canteen = canteen
        self.parking = parking
        self.conference_room = conference_room